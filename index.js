// Static Method
Array.from('🍎🍌🍇');    			// [ '🍎', '🍌', '🍇' ]
Array.isArray(['🍎', '🍌', '🍇']);	// true
Array.of('🍎', '🍌', '🍇');			// [ '🍎', '🍌', '🍇' ]

// Instance Method
['🍎', '🍌'].concat(['🍇', '🍉']);				// [ '🍎', '🍌', '🍇', '🍉' ]
['🍎', '🍌', '🍇', '🍉'].copyWithin(2, 0);		// [ '🍎', '🍌', '🍎', '🍌' ]
['🍎', '🍌', '🍇'].filter(i => i === '🍎');		// [ '🍎' ]
['🍎', '🍌', '🍇'].fill('🍑');					// [ '🍑', '🍑', '🍑' ]
['🍎', '🍌', '🍇'].find(i => i === '🍎');		// 🍎
['🍎', '🍌', '🍇'].indexOf('🍎');				// 0
['🍎', '🍌', '🍇'].findIndex(i => i === '🍎')	// 0
['🍎', '🍌', '🍇'].forEach(i => console.log(i));	// 🍎🍌🍇
['🍎', '🍌', '🍇'].map(i => i + '🍈');	// [ '🍎🍈', '🍌🍈', '🍇🍈' ]
['🍎', '🍌', '🍇'].every(i => i === '🍎');		// false
['🍎', '🍌', '🍇'].some(i => i === '🍎');		// true
['🍎', '🍌', '🍇'].includes('🍎');				// true
['🍎', '🍌', '🍇'].join(" + ");					// 🍎 + 🍌 + 🍇
['🍎', '🍌', '🍇'].pop();						// 🍇 破壊的
['🍎', '🍌', '🍇'].push('🍒');					// 4 破壊的
['🍎', '🍌', '🍇'].reverse();					// [ '🍇', '🍌', '🍎' ]
['🍎', '🍌', '🍇'].splice(1, 2);				// [ '🍌', '🍇'] と [ '🍎' ] 破壊的
['🍎', '🍌', '🍇'].slice(1, 2);					// [ '🍌' ]　と ['🍎', '🍌', '🍇']　非破壊的
['🍎', '🍌', '🍇'].toString();					// 🍎,🍌,🍇
['🍎', '🍌', '🍇'].shift();						// 🍎 破壊的
['🍎', '🍌', '🍇'].unshift('🍉');				// 4  破壊的
['🍎', '🍌', '🍇'].reduce((result, i) => result + i, '🍉');	// 🍉🍎🍌🍇
